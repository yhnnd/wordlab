void ColorfulAllDim(int t){
int tp=t%6;
     if(t ==0)  colorset(bothdarkwhite);
else if(tp==1)  colorset(bothred);
else if(tp==2)  colorset(bothyellow);
else if(tp==3)  colorset(bothgreen);
else if(tp==4)  colorset(bothblue);
else if(tp==5)  colorset(bothcyan);
else if(tp==0)  colorset(bothpurple);
}
